// Função de multiplicação
function multiplicar(a, b) {
    return a * b;
}
// Função de saudação
function saudar(nome) {
    return "Olá " + nome;
}
// Exemplos de uso
var resultadoMultiplicacao = multiplicar(5, 3);
var saudacao = saudar("Maria");
console.log(resultadoMultiplicacao); // Saída: 15
console.log(saudacao); // Saída: Olá Maria
